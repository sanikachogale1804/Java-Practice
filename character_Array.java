class character_Array
{
	public static void main(String args[])
	{
		char ch[]={'a','e','s','g','i','a','o','n','u','a'};
		int count=0;
		for(char c:ch)
		{
			System.out.println(c);
			if(c=='a')
				count++;
		}
		System.out.println(count);
		
		if(ch=='a')
		{
			System.out.println(ch);
		}
		else
		{
			System.out.println("hjgsx");
		}
	}
}